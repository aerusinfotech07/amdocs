package com.amdocs.ass1;

public class LoopSExample {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++)
		{
			System.out.print(i);
		}
		System.out.println("");
		
		int y=1;
		while(y<=5)
		{
			System.out.println(y);
			y++;
		}
		System.out.println("");
		
		int z=1;
		do
		{
			System.out.println(z);
			z++;
		}
		while(z<=5);
		
		

	}

}
